    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if(txtNombre.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre de Producto");
            txtNombre.requestFocus();//pon el foco
            return;
        }
        if(txtDescripcion.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripci√≥n para el producto");
            txtDescripcion.requestFocus();//pon el foco
            return;
        }
        if(txtPrecioVenta.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio para el producto");
            txtPrecioVenta.requestFocus();//pon el foco
            return;
        }

        DProducto dts = new DProducto();
        LProducto func = new LProducto();

        //enviando los datos de los campos a los metodos setter
        dts.setNombre(txtNombre.getText());
        dts.setDescripcion(txtDescripcion.getText());
        int seleccionado = cboUnidadMedida.getSelectedIndex();//almacenando lo q he seleccionado del combopiso
        dts.setUnidadMedida((String)cboUnidadMedida.getItemAt(seleccionado));
        dts.setPrecioVenta(Double.parseDouble(txtPrecioVenta.getText()));
        
        //determinar si la accion a realizar es editar o guardar
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El producto fue registrado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }

        }else if(accion.equals("editar")){

            dts.setIdproducto(Integer.parseInt(txtidProducto.getText()));

            if(func.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El producto fue editado correctamente!");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

