    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //
        try {
            DefaultTableModel modelo;
            
            LTrabajador func = new LTrabajador();
            
            DTrabajador dts = new DTrabajador();
            
            //enviando los datos al objeto dts
            dts.setLogin(txtusuario.getText());
            dts.setPassword(txtpassword.getText());
            
            modelo = func.login(dts.getLogin(), dts.getPassword());//enviando los datos al metodo login 
            
            tablalistado.setModel(modelo);//enviando el modelo a tablalistado
            
            //verificando si el usuario existe o no
            if(func.totalRegistros > 0){//si el usuario existe
                this.dispose();//cierro este form login
                Frm_Inicio form = new Frm_Inicio();
                form.toFront();//aparece al frente frm_inicio
                form.setVisible(true);
                
                //mandando todos los datos almacenados en tablalistado a los labels del MDI
                Frm_Inicio.lblidpersona.setText(tablalistado.getValueAt(0, 0).toString());//enviando al lblidpersona lo que hay en tablalistado(fila0,columna0)
                Frm_Inicio.lblnombre.setText(tablalistado.getValueAt(0, 1).toString());
                Frm_Inicio.lblapaterno.setText(tablalistado.getValueAt(0, 2).toString());
                Frm_Inicio.lblamaterno.setText(tablalistado.getValueAt(0, 3).toString());
                Frm_Inicio.lblacceso.setText(tablalistado.getValueAt(0, 4).toString());
                
                //restringiendo accesos
                if(!Frm_Inicio.lblacceso.getText().equals("Administrador")){//si no es admin
                    //no tendr√° acceso al menuArchivo y menuConfiguraciones
                    Frm_Inicio.menuArchivo.setEnabled(false);
                    Frm_Inicio.menuConfiguraciones.setEnabled(false);
                }
                
            }//si el usuario no existe
            else{
                JOptionPane.showMessageDialog(rootPane,"Acceso Denegado","Acceso al Sistema", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

